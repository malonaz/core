package gateway

import (
	"errors"
	"fmt"
	"strings"

	"google.golang.org/protobuf/compiler/protogen"

	gatewaypb "github.com/malonaz/core/genproto/codegen/gateway/v1"
	"github.com/malonaz/core/go/pbutil"
	"github.com/malonaz/core/tools/protoc-gen-core/plugin"
)

func Generate(file *protogen.File, g *protogen.GeneratedFile, packageName protogen.GoPackageName, opts *plugin.Opts) error {
	type proxyMethod struct {
		service      *protogen.Service
		method       *protogen.Method
		opts         *gatewaypb.HandlerOpts
		proxyService string
		proxyMethod  string
	}

	var methods []proxyMethod
	for _, service := range file.Services {
		for _, method := range service.Methods {
			handlerOpts, err := pbutil.GetExtension[*gatewaypb.HandlerOpts](method.Desc.Options(), gatewaypb.E_Opts)
			if err != nil {
				if errors.Is(err, pbutil.ErrExtensionNotFound) {
					continue
				}
				return fmt.Errorf("getting handler opts for %s: %w", method.GoName, err)
			}
			if handlerOpts.GetProxy() == "" {
				continue
			}
			parts := strings.Split(handlerOpts.GetProxy(), ".")
			methods = append(methods, proxyMethod{
				service:      service,
				method:       method,
				opts:         handlerOpts,
				proxyService: parts[len(parts)-2],
				proxyMethod:  parts[len(parts)-1],
			})
		}
	}

	if len(methods) == 0 {
		g.Skip()
		return nil
	}

	g.P("// Code generated by protoc-gen-api. DO NOT EDIT.")
	g.P("// source: ", file.Desc.Path())
	g.P("package ", packageName)
	g.P()

	grpcPkg := protogen.GoImportPath("github.com/malonaz/core/go/grpc")
	middlewarePkg := protogen.GoImportPath("github.com/malonaz/core/go/grpc/middleware")
	ioPkg := protogen.GoImportPath("io")
	contextPkg := protogen.GoImportPath("context")

	ident := func(path protogen.GoImportPath, name string) string {
		return g.QualifiedGoIdent(protogen.GoIdent{GoName: name, GoImportPath: path})
	}

	for _, pm := range methods {
		svcIdent := func(name string) string {
			return g.QualifiedGoIdent(file.GoImportPath.Ident(name))
		}

		untitledProxy := strings.ToLower(pm.proxyService[:1]) + pm.proxyService[1:]

		fieldMaskLine := ""
		if paths := pm.opts.GetFieldMask().GetPaths(); len(paths) > 0 {
			fieldMaskLine = fmt.Sprintf("ctx = %s(ctx, \"%s\")", ident(middlewarePkg, "WithReadMaskStrict"), strings.Join(paths, ","))
		}

		isServerStream := pm.method.Desc.IsStreamingServer()
		isClientStream := pm.method.Desc.IsStreamingClient()

		if isServerStream && isClientStream {
			// Bidi stream
			streamType := fmt.Sprintf("%s_%sServer", pm.service.GoName, pm.method.GoName)
			g.P("func (h *Service) ", pm.method.GoName, "(srv ", svcIdent(streamType), ") error {")
			g.P("  ctx := srv.Context()")
			g.P("  ctx = ", ident(grpcPkg, "ForwardCustomHeaders"), "(ctx)")
			if fieldMaskLine != "" {
				g.P("  ", fieldMaskLine)
			}
			g.P("  pipeFn := func() error {")
			g.P("    stream, err := h.", untitledProxy, "Client.", pm.proxyMethod, "(ctx)")
			g.P("    if err != nil {")
			g.P("      return err")
			g.P("    }")
			g.P()
			g.P("    errChan := make(chan error, 2)")
			g.P()
			g.P("    go func() {")
			g.P("      defer stream.CloseSend()")
			g.P("      for {")
			g.P("        request, err := srv.Recv()")
			g.P("        if err != nil {")
			g.P("          errChan <- err")
			g.P("          return")
			g.P("        }")
			g.P("        if err := stream.Send(request); err != nil {")
			g.P("          errChan <- err")
			g.P("          return")
			g.P("        }")
			g.P("      }")
			g.P("    }()")
			g.P()
			g.P("    go func() {")
			g.P("      for {")
			g.P("        response, err := stream.Recv()")
			g.P("        if err != nil {")
			g.P("          errChan <- err")
			g.P("          return")
			g.P("        }")
			g.P("        if err := srv.Send(response); err != nil {")
			g.P("          errChan <- err")
			g.P("          return")
			g.P("        }")
			g.P("      }")
			g.P("    }()")
			g.P()
			g.P("    for i := 0; i < 2; i++ {")
			g.P("      if err := <-errChan; err != nil && err != ", ident(ioPkg, "EOF"), " {")
			g.P("        return err")
			g.P("      }")
			g.P("    }")
			g.P("    return nil")
			g.P("  }")
			g.P("  return pipeFn()")
			g.P("}")
		} else if isServerStream {
			// Server stream
			inputType := g.QualifiedGoIdent(pm.method.Input.GoIdent)
			streamType := fmt.Sprintf("%s_%sServer", pm.service.GoName, pm.method.GoName)
			g.P("func (h *Service) ", pm.method.GoName, "(request *", inputType, ", srv ", svcIdent(streamType), ") error {")
			g.P("  ctx := srv.Context()")
			g.P("  ctx = ", ident(grpcPkg, "ForwardCustomHeaders"), "(ctx)")
			if fieldMaskLine != "" {
				g.P("  ", fieldMaskLine)
			}
			g.P("  pipeFn := func() error {")
			g.P("    stream, err := h.", untitledProxy, "Client.", pm.proxyMethod, "(ctx, request)")
			g.P("    if err != nil {")
			g.P("      return err")
			g.P("    }")
			g.P("    for {")
			g.P("      response, err := stream.Recv()")
			g.P("      if err != nil {")
			g.P("        if err == ", ident(ioPkg, "EOF"), " {")
			g.P("          break")
			g.P("        }")
			g.P("        return err")
			g.P("      }")
			g.P("      if err := srv.Send(response); err != nil {")
			g.P("        return err")
			g.P("      }")
			g.P("    }")
			g.P("    return nil")
			g.P("  }")
			g.P("  return pipeFn()")
			g.P("}")
		} else if isClientStream {
			// Client stream
			streamType := fmt.Sprintf("%s_%sServer", pm.service.GoName, pm.method.GoName)
			g.P("func (h *Service) ", pm.method.GoName, "(srv ", svcIdent(streamType), ") error {")
			g.P("  ctx := srv.Context()")
			g.P("  ctx = ", ident(grpcPkg, "ForwardCustomHeaders"), "(ctx)")
			if fieldMaskLine != "" {
				g.P("  ", fieldMaskLine)
			}
			g.P("  pipeFn := func() error {")
			g.P("    stream, err := h.", untitledProxy, "Client.", pm.proxyMethod, "(ctx)")
			g.P("    if err != nil {")
			g.P("      return err")
			g.P("    }")
			g.P()
			g.P("    for {")
			g.P("      request, err := srv.Recv()")
			g.P("      if err != nil {")
			g.P("        if err == ", ident(ioPkg, "EOF"), " {")
			g.P("          break")
			g.P("        }")
			g.P("        return err")
			g.P("      }")
			g.P()
			g.P("      if err := stream.Send(request); err != nil {")
			g.P("        return err")
			g.P("      }")
			g.P("    }")
			g.P()
			g.P("    response, err := stream.CloseAndRecv()")
			g.P("    if err != nil {")
			g.P("      return err")
			g.P("    }")
			g.P()
			g.P("    return srv.SendAndClose(response)")
			g.P("  }")
			g.P("  return pipeFn()")
			g.P("}")
		} else {
			// Unary
			inputType := g.QualifiedGoIdent(pm.method.Input.GoIdent)
			outputType := g.QualifiedGoIdent(pm.method.Output.GoIdent)
			g.P("func (h *Service) ", pm.method.GoName, "(ctx ", ident(contextPkg, "Context"), ", request *", inputType, ") (*", outputType, ", error) {")
			g.P("  ctx = ", ident(grpcPkg, "ForwardCustomHeaders"), "(ctx)")
			if fieldMaskLine != "" {
				g.P("  ", fieldMaskLine)
			}
			g.P("  return h.", untitledProxy, "Client.", pm.proxyMethod, "(ctx, request)")
			g.P("}")
		}
		g.P()
	}

	return nil
}
