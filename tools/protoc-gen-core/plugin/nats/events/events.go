package events

import (
	"errors"
	"fmt"
	"strings"

	"github.com/huandu/xstrings"
	"google.golang.org/protobuf/compiler/protogen"

	natspb "github.com/malonaz/core/genproto/codegen/nats/v1"
	"github.com/malonaz/core/go/pbutil"
	"github.com/malonaz/core/tools/protoc-gen-core/plugin"
	"github.com/malonaz/core/tools/protoc-gen-core/plugin/nats"
	"github.com/malonaz/core/tools/protoc-gen-core/resource"
)

var (
	syncPkg      = protogen.GoImportPath("sync")
	fmtPkg       = protogen.GoImportPath("fmt")
	stringsPkg   = protogen.GoImportPath("strings")
	contextPkg   = protogen.GoImportPath("context")
	celPkg       = protogen.GoImportPath("github.com/google/cel-go/cel")
	celExtPkg    = protogen.GoImportPath("github.com/google/cel-go/ext")
	fieldmaskPkg = protogen.GoImportPath("google.golang.org/protobuf/types/known/fieldmaskpb")
	natsCPkg     = protogen.GoImportPath("github.com/malonaz/core/go/nats")
	aipPkg       = protogen.GoImportPath("github.com/malonaz/core/go/aip")
	resnPkg      = protogen.GoImportPath("go.einride.tech/aip/resourcename")
)

type generator struct {
	g *protogen.GeneratedFile
}

func (gen *generator) ident(path protogen.GoImportPath, name string) string {
	return gen.g.QualifiedGoIdent(protogen.GoIdent{GoName: name, GoImportPath: path})
}

func Generate(file *protogen.File, g *protogen.GeneratedFile, packageName protogen.GoPackageName, opts *plugin.Opts) error {
	gen := &generator{g: g}

	type eventMessage struct {
		message   *protogen.Message
		opts      *natspb.EventOptions
		pattern   *resource.ParsedPattern
		streamFQN string
	}

	streamFQNToGoName := map[string]string{}
	var streamFQNs []string
	var eventMessages []eventMessage

	for _, message := range file.Messages {
		eventOpts, err := pbutil.GetExtension[*natspb.EventOptions](message.Desc.Options(), natspb.E_Event)
		if err != nil {
			if errors.Is(err, pbutil.ErrExtensionNotFound) {
				continue
			}
			return fmt.Errorf("getting nats event opts for %s: %w", message.GoIdent.GoName, err)
		}

		pr, err := resource.ParseFromMessage(message)
		if err != nil {
			return fmt.Errorf("parsing resource from message %s: %w", message.GoIdent.GoName, err)
		}
		pattern, err := pr.SinglePattern()
		if err != nil {
			return err
		}

		streamFQN := eventOpts.GetStream()
		if _, ok := streamFQNToGoName[streamFQN]; !ok {
			streamFQNToGoName[streamFQN] = nats.StreamGoName(streamFQN)
			streamFQNs = append(streamFQNs, streamFQN)
		}

		eventMessages = append(eventMessages, eventMessage{
			message:   message,
			opts:      eventOpts,
			pattern:   pattern,
			streamFQN: streamFQN,
		})
	}

	if len(eventMessages) == 0 {
		g.Skip()
		return nil
	}

	g.P("// Code generated by protoc-gen-api. DO NOT EDIT.")
	g.P("// source: ", file.Desc.Path())
	g.P("package ", packageName)
	g.P()

	// Streams are emitted in first-seen order to keep the output deterministic.
	for _, streamFQN := range streamFQNs {
		nats.GenerateStreamSingleton(g, streamFQN, streamFQNToGoName[streamFQN])
	}

	for _, em := range eventMessages {
		streamGoName := streamFQNToGoName[em.streamFQN]

		patternVarSet := map[string]bool{}
		for _, v := range em.pattern.Variables {
			patternVarSet[v] = true
		}

		for _, seg := range em.opts.GetResourceSegments() {
			if !patternVarSet[seg] {
				return fmt.Errorf("resource_segment %q references unknown resource name segment on %s (pattern: %s)",
					seg, em.message.GoIdent.GoName, em.pattern.Value)
			}
		}

		eventTypeMap := collectEventTypes(em.opts)
		for _, et := range eventTypeMap {
			for _, methodOpt := range et.opts {
				if err := generateSubjectStruct(gen, em.message, em.pattern, em.opts, streamGoName, et.eventType, methodOpt); err != nil {
					return err
				}
			}
		}

		for _, et := range eventTypeMap {
			for _, methodOpt := range et.opts {
				if methodOpt.GetCel() != "" {
					generateCELProgram(gen, em.message, et.eventType, methodOpt)
				}
			}
		}
	}

	return nil
}

type eventTypeEntry struct {
	eventType string
	opts      []*natspb.EventMethodOptions
}

func collectEventTypes(eventOpts *natspb.EventOptions) []eventTypeEntry {
	var result []eventTypeEntry
	if opts := eventOpts.GetCreated(); len(opts) > 0 {
		result = append(result, eventTypeEntry{eventType: "Created", opts: opts})
	}
	if opts := eventOpts.GetUpdated(); len(opts) > 0 {
		result = append(result, eventTypeEntry{eventType: "Updated", opts: opts})
	}
	if opts := eventOpts.GetDeleted(); len(opts) > 0 {
		result = append(result, eventTypeEntry{eventType: "Deleted", opts: opts})
	}
	return result
}

func generateSubjectStruct(gen *generator, message *protogen.Message, pattern *resource.ParsedPattern, eventOpts *natspb.EventOptions, streamGoName, eventType string, methodOpt *natspb.EventMethodOptions) error {
	g := gen.g
	structName := message.GoIdent.GoName + xstrings.ToPascalCase(methodOpt.GetSubject()) + "Subject"
	celProgramVar := xstrings.ToCamelCase(message.GoIdent.GoName+xstrings.ToPascalCase(methodOpt.GetSubject())) + "CELProgram"
	resourceSegments := eventOpts.GetResourceSegments()
	subjectFields := methodOpt.GetSubjectFields()
	protoType := gen.g.QualifiedGoIdent(message.GoIdent)

	for _, sf := range subjectFields {
		if fieldByTextName(message, sf) == nil {
			return fmt.Errorf("subject_field %q not found on message %s", sf, message.GoIdent.GoName)
		}
	}

	// Struct definition.
	g.P("type ", structName, " struct {")
	g.P("  stream *", streamGoName)
	for _, seg := range resourceSegments {
		g.P("  _", xstrings.ToCamelCase(seg), " *string")
	}
	for _, sf := range subjectFields {
		field := fieldByTextName(message, sf)
		if field.Enum != nil {
			g.P("  _", xstrings.ToCamelCase(field.GoName), " *", gen.g.QualifiedGoIdent(field.Enum.GoIdent))
		} else {
			g.P("  _", xstrings.ToCamelCase(field.GoName), " *string")
		}
	}
	g.P("}")
	g.P()

	// With* methods for resource segments.
	for _, seg := range resourceSegments {
		goField := xstrings.ToCamelCase(seg)
		g.P("func (s *", structName, ") With", xstrings.ToPascalCase(seg), "(v string) *", structName, " {")
		g.P("  s._", goField, " = &v")
		g.P("  return s")
		g.P("}")
		g.P()
	}

	// With* methods for subject fields.
	for _, sf := range subjectFields {
		field := fieldByTextName(message, sf)
		if field.Enum != nil {
			enumType := gen.g.QualifiedGoIdent(field.Enum.GoIdent)
			g.P("func (s *", structName, ") With", xstrings.ToPascalCase(sf), "(v ", enumType, ") *", structName, " {")
			g.P("  s._", xstrings.ToCamelCase(field.GoName), " = &v")
			g.P("  return s")
			g.P("}")
		} else {
			g.P("func (s *", structName, ") With", xstrings.ToPascalCase(sf), "(v string) *", structName, " {")
			g.P("  s._", xstrings.ToCamelCase(field.GoName), " = &v")
			g.P("  return s")
			g.P("}")
		}
		g.P()
	}

	// evaluate method.
	if eventType == "Updated" {
		g.P("func (s *", structName, ") evaluate(resource *", protoType, ", previousResource *", protoType, ", updateMask *", gen.ident(fieldmaskPkg, "FieldMask"), ") (bool, error) {")
	} else {
		g.P("func (s *", structName, ") evaluate(resource *", protoType, ") (bool, error) {")
	}
	if methodOpt.GetCel() != "" {
		g.P("  out, _, err := get", xstrings.ToPascalCase(celProgramVar), "().Eval(map[string]any{")
		g.P("    \"", xstrings.ToSnakeCase(message.GoIdent.GoName), "\": resource,")
		if eventType == "Updated" {
			g.P("    \"previous_", xstrings.ToSnakeCase(message.GoIdent.GoName), "\": previousResource,")
			g.P("    \"update_mask\": updateMask,")
		}
		g.P("  })")
		g.P("  if err != nil {")
		g.P("    return false, ", gen.ident(fmtPkg, "Errorf"), "(\"evaluating CEL expression: %w\", err)")
		g.P("  }")
		g.P("  result, ok := out.Value().(bool)")
		g.P("  if !ok {")
		g.P("    return false, ", gen.ident(fmtPkg, "Errorf"), "(\"CEL expression returned non-bool type %T\", out.Value())")
		g.P("  }")
		g.P("  return result, nil")
	} else {
		g.P("  return true, nil")
	}
	g.P("}")
	g.P()

	// Publish method.
	if eventType == "Updated" {
		g.P("func (s *", structName, ") Publish(ctx ", gen.ident(contextPkg, "Context"), ", natsClient *", gen.ident(natsCPkg, "Client"), ", resource *", protoType, ", previousResource *", protoType, ", updateMask *", gen.ident(fieldmaskPkg, "FieldMask"), ") error {")
		g.P("  ok, err := s.evaluate(resource, previousResource, updateMask)")
	} else {
		g.P("func (s *", structName, ") Publish(ctx ", gen.ident(contextPkg, "Context"), ", natsClient *", gen.ident(natsCPkg, "Client"), ", resource *", protoType, ") error {")
		g.P("  ok, err := s.evaluate(resource)")
	}
	g.P("  if err != nil {")
	g.P("    return err")
	g.P("  }")
	g.P("  if !ok {")
	g.P("    return nil")
	g.P("  }")
	g.P("  if err := s.set(resource); err != nil {")
	g.P("    return err")
	g.P("  }")
	g.P("  subject, err := s.Get()")
	g.P("  if err != nil {")
	g.P("    return ", gen.ident(fmtPkg, "Errorf"), "(\"getting subject: %w\", err)")
	g.P("  }")
	if eventType == "Updated" {
		g.P("  event, err := ", gen.ident(aipPkg, "NewResourceUpdatedEvent"), "(resource, previousResource, updateMask)")
	} else if eventType == "Created" {
		g.P("  event, err := ", gen.ident(aipPkg, "NewResourceCreatedEvent"), "(resource)")
	} else {
		g.P("  event, err := ", gen.ident(aipPkg, "NewResourceDeletedEvent"), "(resource)")
	}
	g.P("  if err != nil {")
	g.P("    return ", gen.ident(fmtPkg, "Errorf"), "(\"constructing resource event: %w\", err)")
	g.P("  }")
	g.P("  return natsClient.Publish(ctx, subject, event)")
	g.P("}")
	g.P()

	// set method.
	g.P("func (s *", structName, ") set(resource *", protoType, ") error {")
	if len(resourceSegments) > 0 {
		varDecls := make([]string, 0, len(pattern.Variables))
		for _, pv := range pattern.Variables {
			varDecls = append(varDecls, xstrings.ToCamelCase(pv)+"ID")
		}
		g.P("  var ", strings.Join(varDecls, ", "), " string")
		addrArgs := ""
		for _, v := range varDecls {
			addrArgs += ", &" + v
		}
		g.P("  if err := ", gen.ident(resnPkg, "Sscan"), "(resource.GetName(), \"", pattern.Value, "\"", addrArgs, "); err != nil {")
		g.P("    return ", gen.ident(fmtPkg, "Errorf"), "(\"parsing resource name: %v\", err)")
		g.P("  }")
		for _, seg := range resourceSegments {
			g.P("  s.With", xstrings.ToPascalCase(seg), "(", xstrings.ToCamelCase(seg), "ID)")
		}
	}
	for _, sf := range subjectFields {
		field := fieldByTextName(message, sf)
		g.P("  s.With", xstrings.ToPascalCase(sf), "(resource.", field.GoName, ")")
	}
	g.P("  return nil")
	g.P("}")
	g.P()

	// Get method — validates set fields are non-empty, uses "*" for unset fields.
	g.P("func (s *", structName, ") Get() (*", gen.ident(natsCPkg, "Subject"), ", error) {")
	for _, seg := range resourceSegments {
		goField := xstrings.ToCamelCase(seg)
		g.P("  if s._", goField, " != nil && *s._", goField, " == \"\" {")
		g.P("    return nil, ", gen.ident(fmtPkg, "Errorf"), "(\"", seg, " must not be empty\")")
		g.P("  }")
	}

	for _, sf := range subjectFields {
		field := fieldByTextName(message, sf)
		goField := xstrings.ToCamelCase(field.GoName)
		if field.Enum != nil {
			g.P("  if s._", goField, " != nil && *s._", goField, " == 0 {")
			g.P("    return nil, ", gen.ident(fmtPkg, "Errorf"), "(\"", sf, " must not be unspecified\")")
			g.P("  }")
		} else {
			g.P("  if s._", goField, " != nil && *s._", goField, " == \"\" {")
			g.P("    return nil, ", gen.ident(fmtPkg, "Errorf"), "(\"", sf, " must not be empty\")")
			g.P("  }")
		}
	}
	g.P("  tokens := []string{")
	for _, seg := range resourceSegments {
		goField := xstrings.ToCamelCase(seg)
		g.P("    func() string {")
		g.P("      if s._", goField, " != nil {")
		g.P("        return *s._", goField)
		g.P("      }")
		g.P("      return \"*\"")
		g.P("    }(),")
	}
	g.P("    \"", strings.ToLower(methodOpt.GetSubject()), "\",")
	for _, sf := range subjectFields {
		field := fieldByTextName(message, sf)
		goField := xstrings.ToCamelCase(field.GoName)
		g.P("    func() string {")
		g.P("      if s._", goField, " != nil {")
		if field.Enum != nil {
			enumPrefix := strings.ToUpper(xstrings.ToSnakeCase(field.Enum.GoIdent.GoName)) + "_"
			g.P("        return ", gen.ident(stringsPkg, "ToLower"), "(", gen.ident(stringsPkg, "TrimPrefix"), "(s._", goField, ".String(), \"", enumPrefix, "\"))")
		} else {
			g.P("        return *s._", goField)
		}
		g.P("      }")
		g.P("      return \"*\"")
		g.P("    }(),")
	}
	g.P("  }")
	g.P("  return s.stream.stream.Subject(", gen.ident(stringsPkg, "Join"), "(tokens, \".\")), nil")
	g.P("}")
	g.P()

	// MustGet method.
	g.P("func (s *", structName, ") MustGet() *", gen.ident(natsCPkg, "Subject"), " {")
	g.P("  subject, err := s.Get()")
	g.P("  if err != nil {")
	g.P("    panic(err)")
	g.P("  }")
	g.P("  return subject")
	g.P("}")
	g.P()

	// Stream accessor.
	g.P("func (s *", streamGoName, ") Get", xstrings.ToPascalCase(methodOpt.GetSubject()), "Subject() *", structName, " {")
	g.P("  return &", structName, "{stream: s}")
	g.P("}")
	g.P()

	return nil
}

func generateCELProgram(gen *generator, message *protogen.Message, eventType string, methodOpt *natspb.EventMethodOptions) {
	g := gen.g
	varName := xstrings.ToCamelCase(message.GoIdent.GoName+xstrings.ToPascalCase(methodOpt.GetSubject())) + "CELProgram"
	protoType := gen.g.QualifiedGoIdent(message.GoIdent)

	g.P("var (")
	g.P("  ", varName, "Once ", gen.ident(syncPkg, "Once"))
	g.P("  ", varName, "Val ", gen.ident(celPkg, "Program"))
	g.P(")")
	g.P()

	g.P("func get", xstrings.ToPascalCase(varName), "() ", gen.ident(celPkg, "Program"), " {")
	g.P("  ", varName, "Once.Do(func() {")
	g.P("    env, err := ", gen.ident(celPkg, "NewEnv"), "(")
	g.P("      ", gen.ident(celExtPkg, "Protos"), "(),")
	g.P("      ", gen.ident(celPkg, "Types"), "(&", protoType, "{}),")
	g.P("      ", gen.ident(celPkg, "Types"), "(&", gen.ident(fieldmaskPkg, "FieldMask"), "{}),")
	g.P("      ", gen.ident(celPkg, "Variable"), "(\"", xstrings.ToSnakeCase(message.GoIdent.GoName), "\", ", gen.ident(celPkg, "ObjectType"), "(\"", message.Desc.FullName(), "\")),")
	if eventType == "Updated" {
		g.P("      ", gen.ident(celPkg, "Variable"), "(\"previous_", xstrings.ToSnakeCase(message.GoIdent.GoName), "\", ", gen.ident(celPkg, "ObjectType"), "(\"", message.Desc.FullName(), "\")),")
		g.P("      ", gen.ident(celPkg, "Variable"), "(\"update_mask\", ", gen.ident(celPkg, "ObjectType"), "(\"google.protobuf.FieldMask\")),")
	}
	g.P("    )")
	g.P("    if err != nil {")
	g.P("      panic(", gen.ident(fmtPkg, "Sprintf"), "(\"creating CEL environment: %v\", err))")
	g.P("    }")
	g.P("    ast, issues := env.Compile(`", methodOpt.GetCel(), "`)")
	g.P("    if issues != nil && issues.Err() != nil {")
	g.P("      panic(", gen.ident(fmtPkg, "Sprintf"), "(\"compiling CEL expression: %v\", issues.Err()))")
	g.P("    }")
	g.P("    if ast.OutputType() != ", gen.ident(celPkg, "BoolType"), " {")
	g.P("      panic(", gen.ident(fmtPkg, "Sprintf"), "(\"CEL expression must return bool, got %v\", ast.OutputType()))")
	g.P("    }")
	g.P("    prg, err := env.Program(ast)")
	g.P("    if err != nil {")
	g.P("      panic(", gen.ident(fmtPkg, "Sprintf"), "(\"creating CEL program: %v\", err))")
	g.P("    }")
	g.P("    ", varName, "Val = prg")
	g.P("  })")
	g.P("  return ", varName, "Val")
	g.P("}")
	g.P()
}

func fieldByTextName(message *protogen.Message, textName string) *protogen.Field {
	for _, f := range message.Fields {
		if f.Desc.TextName() == textName {
			return f
		}
	}
	return nil
}
