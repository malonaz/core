func Generate(file *protogen.File, generatedFile *protogen.GeneratedFile, packageName protogen.GoPackageName, opts *plugin.Opts) error {
	generatedFile.P("// Code generated by protoc-gen-api. DO NOT EDIT.")
	generatedFile.P("// source: ", file.Desc.Path())
	generatedFile.P("package ", packageName)
	generatedFile.P()

	for _, message := range file.Messages {
		m, err := Parse(message, generatedFile)
		if err != nil {
			return err
		}
		if m == nil {
			continue
		}
		generatedFile.P(m.ErrorVarsDef())
		s, err := m.StructDef()
		if err != nil {
			return err
		}
		generatedFile.P(s)
		s, err = m.FromPbDef()
		if err != nil {
			return err
		}
		generatedFile.P(s)
		s, err = m.ToPbDef()
		if err != nil {
			return err
		}
		generatedFile.P(s)
		s, err = m.ParseNameDef()
		if err != nil {
			return err
		}
		generatedFile.P(s)
	}
	return nil
}

type Model struct {
	Message        *protogen.Message
	ModelOpts      *modelpb.ModelOpts
	ParsedResource *resource.ParsedResource
	Patterns       []*resource.ParsedPattern
	MultiPattern   bool
	Table          schema.Table
	Bindings       []schema.ColumnBinding
	Fields         []*Field
	generatedFile  *protogen.GeneratedFile
}

type Field struct {
	ProtoField *protogen.Field
	FieldOpts  *modelpb.FieldOpts
	Skipped    bool
	IsName     bool
}

func Parse(message *protogen.Message, generatedFile *protogen.GeneratedFile) (*Model, error) {
	modelOpts, err := pbutil.GetExtension[*modelpb.ModelOpts](message.Desc.Options(), modelpb.E_ModelOpts)
	if err != nil {
		if errors.Is(err, pbutil.ErrExtensionNotFound) {
			return nil, nil
		}
		return nil, fmt.Errorf("getting model_opts for %s: %w", message.GoIdent.GoName, err)
	}

	parsedResource, err := resource.ParseFromMessage(message)
	if err != nil {
		return nil, fmt.Errorf("parsing resource from message %s: %w", message.GoIdent.GoName, err)
	}

	bindings, err := schema.UnionColumnBindings(parsedResource, modelOpts)
	if err != nil {
		return nil, fmt.Errorf("resolving column bindings for %s: %w", message.GoIdent.GoName, err)
	}

	fields, err := parseFields(message)
	if err != nil {
		return nil, fmt.Errorf("parsing fields for %s: %w", message.GoIdent.GoName, err)
	}

	return &Model{
		Message:        message,
		ModelOpts:      modelOpts,
		ParsedResource: parsedResource,
		Patterns:       parsedResource.Patterns,
		MultiPattern:   len(parsedResource.Patterns) > 1,
		Table:          schema.TableOf(parsedResource, modelOpts),
		Bindings:       bindings,
		Fields:         fields,
		generatedFile:  generatedFile,
	}, nil
}

func parseFields(message *protogen.Message) ([]*Field, error) {
	var fields []*Field
	for _, protoField := range message.Fields {
		fieldOpts, err := pbutil.GetExtension[*modelpb.FieldOpts](protoField.Desc.Options(), modelpb.E_FieldOpts)
		if err != nil && !errors.Is(err, pbutil.ErrExtensionNotFound) {
			return nil, fmt.Errorf("field %s has invalid field_opts: %w", protoField.Desc.Name(), err)
		}
		fields = append(fields, &Field{
			ProtoField: protoField,
			FieldOpts:  fieldOpts,
			Skipped:    fieldOpts.GetSkip(),
			IsName:     protoField.Desc.TextName() == "name",
		})
	}
	return fields, nil
}

// --- import helpers ---

func (m *Model) fqn(importPath, name string) string {
	return m.generatedFile.QualifiedGoIdent(protogen.GoIdent{
		GoName:       name,
		GoImportPath: protogen.GoImportPath(importPath),
	})
}

func (m *Model) qgi(ident protogen.GoIdent) string {
	return m.generatedFile.QualifiedGoIdent(ident)
}

// --- accessors ---

func (m *Model) GoName() string {
	return m.Message.GoIdent.GoName
}

func (m *Model) HasEtag() bool {
	return m.Message.Desc.Fields().ByName("etag") != nil
}

func (m *Model) ActiveFields() []*Field {
	var result []*Field
	for _, field := range m.Fields {
		if field.Skipped || field.IsName {
			continue
		}
		result = append(result, field)
	}
	return result
}

func (m *Model) resourceIDFieldNames() []string {
	names := make([]string, len(m.Bindings))
	for i, binding := range m.Bindings {
		names[i] = binding.GoFieldName()
	}
	return names
}

func (m *Model) bindingsByVariable() map[string]schema.ColumnBinding {
	variableToBinding := make(map[string]schema.ColumnBinding, len(m.Bindings))
	for _, binding := range m.Bindings {
		variableToBinding[binding.Variable] = binding
	}
	return variableToBinding
}

// --- code generation ---

func (m *Model) ErrorVarsDef() string {
	var b strings.Builder
	goName := m.GoName()
	snakeName := m.ParsedResource.SingularSnakeCase()
	errorsNew := m.fqn("errors", "New")

	fmt.Fprintf(&b, "var Err%sAlreadyExists = %s(\"%s already exists\")\n", goName, errorsNew, snakeName)
	fmt.Fprintf(&b, "var Err%sNotExist = %s(\"%s does not exist\")\n", goName, errorsNew, snakeName)
	fmt.Fprintf(&b, "var Err%sAlreadyDeleted = %s(\"%s already deleted\")\n", goName, errorsNew, snakeName)
	if m.HasEtag() {
		fmt.Fprintf(&b, "var Err%sETagChanged = %s(\"%s etag changed\")\n", goName, errorsNew, snakeName)
	}
	return b.String()
}

func (m *Model) StructDef() (string, error) {
	var b strings.Builder
	goName := m.GoName()
	pgvector := "github.com/pgvector/pgvector-go"

	fmt.Fprintf(&b, "type %s struct {\n", goName)

	for _, binding := range m.Bindings {
		// Pattern-specific identifiers are nullable: a row only populates the
		// identifiers of the pattern its name was created under.
		goType := "string"
		if !binding.Shared {
			goType = "*string"
		}
		fmt.Fprintf(&b, "\t%s %s `db:\"%s\" schema:\"%s\" table:\"%s\"`\n",
			binding.GoFieldName(), goType, binding.Column, m.Table.SchemaOrPublic(), m.Table.Name)
	}

	for _, field := range m.ActiveFields() {
		if field.FieldOpts.GetEmbed() {
			fmt.Fprintf(&b, "\t%s%s\n", goName, field.ProtoField.GoName)
			continue
		}

		typeName, err := m.resolveFieldType(field.ProtoField, field.FieldOpts, pgvector)
		if err != nil {
			return "", fmt.Errorf("resolving type for %s: %w", field.ProtoField.GoName, err)
		}

		if join := field.FieldOpts.GetJoin(); join != nil {
			target, err := schema.ResolveJoin(join)
			if err != nil {
				return "", fmt.Errorf("resolving join for %s: %w", field.ProtoField.GoName, err)
			}
			fieldNullable := field.FieldOpts.GetNullable()
			if fieldNullable != target.Nullable {
				return "", fmt.Errorf(
					"field %s has nullable=%v but parent field %q on %q has nullable=%v; they must match",
					field.ProtoField.GoName, fieldNullable, join.Field, join.Parent, target.Nullable,
				)
			}
			fmt.Fprintf(&b, "\t%s %s `db:\"%s\" external:\"true\" join_schema:\"%s\" join_table:\"%s\" join_column:\"%s\"`\n",
				field.ProtoField.GoName, typeName, field.ProtoField.Desc.TextName(), target.Table.SchemaOrPublic(), target.Table.Name, target.Column)
			continue
		}

		columnTag, err := m.columnName(field.ProtoField)
		if err != nil {
			return "", fmt.Errorf("resolving column name for %s: %w", field.ProtoField.GoName, err)
		}

		fmt.Fprintf(&b, "\t%s %s `db:\"%s\" schema:\"%s\" table:\"%s\"`\n",
			field.ProtoField.GoName, typeName, columnTag, m.Table.SchemaOrPublic(), m.Table.Name)
	}

	b.WriteString("}\n")
	return b.String(), nil
}

func (m *Model) FromPbDef() (string, error) {
	var b strings.Builder
	goName := m.GoName()
	protoType := m.qgi(m.Message.GoIdent)
	pgvector := "github.com/pgvector/pgvector-go"

	fmt.Fprintf(&b, "func %sFromPb(m *%s) (*%s, error) {\n", goName, protoType, goName)
	fmt.Fprintf(&b, "\tif m.Name == \"\" {\n")
	fmt.Fprintf(&b, "\t\treturn nil, fmt.Errorf(\"FromPb requires `name` to be set\")\n")
	fmt.Fprintf(&b, "\t}\n\n")

	resourceIDFields := m.resourceIDFieldNames()
	if len(resourceIDFields) > 0 {
		fmt.Fprintf(&b, "\t%s, err := Parse%sName(m.Name)\n", strings.Join(resourceIDFields, ", "), goName)
		fmt.Fprintf(&b, "\tif err != nil {\n")
		fmt.Fprintf(&b, "\t\treturn nil, err\n")
		fmt.Fprintf(&b, "\t}\n\n")

		// Pattern-specific identifiers persist as NULL when absent.
		for _, binding := range m.Bindings {
			if binding.Shared {
				continue
			}
			fieldName := binding.GoFieldName()
			fmt.Fprintf(&b, "\tvar %sPtr *string\n", fieldName)
			fmt.Fprintf(&b, "\tif %s != \"\" {\n", fieldName)
			fmt.Fprintf(&b, "\t\t%sPtr = &%s\n", fieldName, fieldName)
			fmt.Fprintf(&b, "\t}\n")
		}
	}

	for _, field := range m.ActiveFields() {
		code, err := m.fromPbFieldConversion(field.ProtoField, field.FieldOpts, goName, pgvector)
		if err != nil {
			return "", fmt.Errorf("field %s: %w", field.ProtoField.GoName, err)
		}
		if code != "" {
			b.WriteString(code)
		}
	}

	fmt.Fprintf(&b, "\treturn &%s{\n", goName)
	for _, binding := range m.Bindings {
		valueName := binding.GoFieldName()
		if !binding.Shared {
			valueName += "Ptr"
		}
		fmt.Fprintf(&b, "\t\t%s: %s,\n", binding.GoFieldName(), valueName)
	}
	for _, field := range m.ActiveFields() {
		fieldPrefix := ""
		if field.FieldOpts.GetEmbed() {
			fieldPrefix = goName
		}
		valueName := m.fromPbFieldValue(field.ProtoField, field.FieldOpts, fieldPrefix)
		fmt.Fprintf(&b, "\t\t%s%s: %s,\n", fieldPrefix, field.ProtoField.GoName, valueName)
	}
	fmt.Fprintf(&b, "\t}, nil\n")
	fmt.Fprintf(&b, "}\n")
	return b.String(), nil
}

func (m *Model) ToPbDef() (string, error) {
	var b strings.Builder
	goName := m.GoName()
	protoType := m.qgi(m.Message.GoIdent)

	fmt.Fprintf(&b, "func (m *%s) ToPb() (*%s, error) {\n", goName, protoType)

	for _, field := range m.ActiveFields() {
		code, err := m.toPbFieldConversion(field.ProtoField, field.FieldOpts, goName)
		if err != nil {
			return "", fmt.Errorf("field %s: %w", field.ProtoField.GoName, err)
		}
		if code != "" {
			b.WriteString(code)
		}
	}

	if m.MultiPattern {
		nameCode, err := m.multiPatternNameConstruction()
		if err != nil {
			return "", err
		}
		b.WriteString(nameCode)
	} else {
		fmt.Fprintf(&b, "\tname := %s(\"%s\"", m.fqn("go.einride.tech/aip/resourcename", "Sprint"), m.Patterns[0].Value)
		for _, idField := range m.resourceIDFieldNames() {
			fmt.Fprintf(&b, ", m.%s", idField)
		}
		fmt.Fprintf(&b, ")\n")
	}
	fmt.Fprintf(&b, "\tif err := %s(name); err != nil {\n", m.fqn("go.einride.tech/aip/resourcename", "Validate"))
	fmt.Fprintf(&b, "\t\treturn nil, fmt.Errorf(\"validating resource name: %%w\", err)\n")
	fmt.Fprintf(&b, "\t}\n")

	fmt.Fprintf(&b, "\treturn &%s{\n", protoType)
	fmt.Fprintf(&b, "\t\tName: name,\n")
	for _, field := range m.ActiveFields() {
		valueName := m.toPbFieldValue(field.ProtoField, field.FieldOpts)
		fmt.Fprintf(&b, "\t\t%s: %s,\n", field.ProtoField.GoName, valueName)
	}
	fmt.Fprintf(&b, "\t}, nil\n")
	fmt.Fprintf(&b, "}\n")
	return b.String(), nil
}

// multiPatternNameConstruction emits a switch selecting the resource name
// pattern from whichever pattern-specific identifiers are set.
func (m *Model) multiPatternNameConstruction() (string, error) {
	var b strings.Builder
	variableToBinding := m.bindingsByVariable()
	sprint := m.fqn("go.einride.tech/aip/resourcename", "Sprint")

	type patternCase struct {
		pattern    *resource.ParsedPattern
		conditions []string
	}
	var cases []patternCase
	var defaultPattern *resource.ParsedPattern
	for _, pattern := range m.Patterns {
		var conditions []string
		for _, variable := range pattern.Variables {
			binding := variableToBinding[variable]
			if !binding.Shared {
				conditions = append(conditions, fmt.Sprintf("m.%s != nil", binding.GoFieldName()))
			}
		}
		if len(conditions) == 0 {
			if defaultPattern != nil {
				return "", fmt.Errorf("multiple patterns of %s have no discriminating variable", m.GoName())
			}
			defaultPattern = pattern
			continue
		}
		cases = append(cases, patternCase{pattern: pattern, conditions: conditions})
	}
	// Most specific patterns first: a row satisfying a deeper pattern's
	// identifiers also satisfies every prefix pattern's identifiers.
	sort.SliceStable(cases, func(i, j int) bool {
		return len(cases[i].conditions) > len(cases[j].conditions)
	})

	fmt.Fprintf(&b, "\tvar name string\n")
	fmt.Fprintf(&b, "\tswitch {\n")
	for _, patternCase := range cases {
		fmt.Fprintf(&b, "\tcase %s:\n", strings.Join(patternCase.conditions, " && "))
		fmt.Fprintf(&b, "\t\tname = %s(\"%s\"%s)\n", sprint, patternCase.pattern.Value, m.sprintArgs(patternCase.pattern, variableToBinding))
	}
	fmt.Fprintf(&b, "\tdefault:\n")
	if defaultPattern != nil {
		fmt.Fprintf(&b, "\t\tname = %s(\"%s\"%s)\n", sprint, defaultPattern.Value, m.sprintArgs(defaultPattern, variableToBinding))
	} else {
		fmt.Fprintf(&b, "\t\treturn nil, %s(\"cannot determine resource name pattern for %s\")\n",
			m.fqn("fmt", "Errorf"), m.ParsedResource.SingularSnakeCase())
	}
	fmt.Fprintf(&b, "\t}\n")
	return b.String(), nil
}

func (m *Model) sprintArgs(pattern *resource.ParsedPattern, variableToBinding map[string]schema.ColumnBinding) string {
	var b strings.Builder
	for _, variable := range pattern.Variables {
		binding := variableToBinding[variable]
		if binding.Shared {
			fmt.Fprintf(&b, ", m.%s", binding.GoFieldName())
		} else {
			fmt.Fprintf(&b, ", *m.%s", binding.GoFieldName())
		}
	}
	return b.String()
}

func (m *Model) ParseNameDef() (string, error) {
	if m.MultiPattern {
		return m.multiPatternParseNameDef()
	}

	var b strings.Builder
	goName := m.GoName()
	resourceIDFields := m.resourceIDFieldNames()

	returnTypes := make([]string, len(resourceIDFields))
	for i := range resourceIDFields {
		returnTypes[i] = "string"
	}
	returnTypes = append(returnTypes, "error")
	fmt.Fprintf(&b, "func Parse%sName(name string) (%s) {\n", goName, strings.Join(returnTypes, ", "))

	fmt.Fprintf(&b, "\tvar %s string\n", strings.Join(resourceIDFields, ", "))

	addrArgs := make([]string, len(resourceIDFields))
	for i, idField := range resourceIDFields {
		addrArgs[i] = "&" + idField
	}
	fmt.Fprintf(&b, "\tif err := %s(name, \"%s\", %s); err != nil {\n",
		m.fqn("go.einride.tech/aip/resourcename", "Sscan"), m.Patterns[0].Value, strings.Join(addrArgs, ", "))

	emptyStrings := make([]string, len(resourceIDFields))
	for i := range emptyStrings {
		emptyStrings[i] = `""`
	}
	fmt.Fprintf(&b, "\t\treturn %s, %s(\"parsing resource name: %%v\", err)\n",
		strings.Join(emptyStrings, ", "), m.fqn("fmt", "Errorf"))
	fmt.Fprintf(&b, "\t}\n")

	fmt.Fprintf(&b, "\treturn %s, nil\n", strings.Join(resourceIDFields, ", "))
	fmt.Fprintf(&b, "}\n")
	return b.String(), nil
}

// multiPatternParseNameDef emits a parser attempting each pattern in
// declaration order, returning "" for the identifiers absent from the matched
// pattern.
func (m *Model) multiPatternParseNameDef() (string, error) {
	var b strings.Builder
	goName := m.GoName()
	unionFields := m.resourceIDFieldNames()
	variableToBinding := m.bindingsByVariable()
	sscan := m.fqn("go.einride.tech/aip/resourcename", "Sscan")
	match := m.fqn("go.einride.tech/aip/resourcename", "Match")

	returnTypes := make([]string, len(unionFields))
	for i := range unionFields {
		returnTypes[i] = "string"
	}
	returnTypes = append(returnTypes, "error")
	fmt.Fprintf(&b, "func Parse%sName(name string) (%s) {\n", goName, strings.Join(returnTypes, ", "))

	emptyStrings := make([]string, len(unionFields))
	for i := range emptyStrings {
		emptyStrings[i] = `""`
	}

	fmt.Fprintf(&b, "\tswitch {\n")
	for _, pattern := range m.Patterns {
		patternFields := make([]string, len(pattern.Variables))
		patternFieldSet := map[string]bool{}
		for i, variable := range pattern.Variables {
			patternFields[i] = variableToBinding[variable].GoFieldName()
			patternFieldSet[patternFields[i]] = true
		}
		addrArgs := make([]string, len(patternFields))
		for i, patternField := range patternFields {
			addrArgs[i] = "&" + patternField
		}
		returns := make([]string, 0, len(unionFields)+1)
		for _, unionField := range unionFields {
			if patternFieldSet[unionField] {
				returns = append(returns, unionField)
			} else {
				returns = append(returns, `""`)
			}
		}
		returns = append(returns, "nil")

		fmt.Fprintf(&b, "\tcase %s(\"%s\", name):\n", match, pattern.Value)
		fmt.Fprintf(&b, "\t\tvar %s string\n", strings.Join(patternFields, ", "))
		fmt.Fprintf(&b, "\t\tif err := %s(name, \"%s\", %s); err != nil {\n", sscan, pattern.Value, strings.Join(addrArgs, ", "))
		fmt.Fprintf(&b, "\t\t\treturn %s, %s(\"parsing resource name: %%v\", err)\n",
			strings.Join(emptyStrings, ", "), m.fqn("fmt", "Errorf"))
		fmt.Fprintf(&b, "\t\t}\n")
		fmt.Fprintf(&b, "\t\treturn %s\n", strings.Join(returns, ", "))
	}
	fmt.Fprintf(&b, "\tdefault:\n")
	fmt.Fprintf(&b, "\t\treturn %s, %s(\"parsing resource name: %%q does not match any pattern of %s\", name)\n",
		strings.Join(emptyStrings, ", "), m.fqn("fmt", "Errorf"), m.ParsedResource.SingularSnakeCase())
	fmt.Fprintf(&b, "\t}\n")
	fmt.Fprintf(&b, "}\n")
	return b.String(), nil
}

// --- field type resolution ---

func (m *Model) columnName(field *protogen.Field) (string, error) {
	name := field.Desc.TextName()
	if field.Desc.Options() == nil {
		return name, nil
	}
	fieldOpts, err := pbutil.GetExtension[*modelpb.FieldOpts](field.Desc.Options(), modelpb.E_FieldOpts)
	if err != nil {
		if errors.Is(err, pbutil.ErrExtensionNotFound) {
			return name, nil
		}
		return "", fmt.Errorf("getting field_opts for column name on %s: %w", field.Desc.Name(), err)
	}
	if fieldOpts.ColumnName != "" {
		return fieldOpts.ColumnName, nil
	}
	return name, nil
}

func (m *Model) resolveFieldType(field *protogen.Field, fieldOpts *modelpb.FieldOpts, pgvector string) (string, error) {
	nullable := fieldOpts.GetNullable()

	if fieldOpts.GetAsJsonBytes() || fieldOpts.GetAsProtoBytes() {
		return "[]byte", nil
	}
	if fieldOpts.GetPgVector() {
		typeName := m.fqn(pgvector, "Vector")
		if nullable {
			return "*" + typeName, nil
		}
		return typeName, nil
	}

	prefix := ""
	if nullable {
		prefix = "*"
	}
	t, err := protofield.SanitizedGoType(field, m.fqn)
	if err != nil {
		return "", fmt.Errorf("resolving go type for %s: %w", field.GoName, err)
	}
	return prefix + t, nil
}

// --- marshal/unmarshal helpers ---

func (m *Model) marshalLib(fieldOpts *modelpb.FieldOpts, field *protogen.Field) (string, string) {
	if field.Desc.IsMap() {
		return "encoding/json", "Marshal"
	}
	if fieldOpts.GetAsJsonBytes() {
		return "github.com/malonaz/core/go/pbutil", "JSONMarshal"
	}
	return "github.com/malonaz/core/go/pbutil", "Marshal"
}

func (m *Model) unmarshalLib(fieldOpts *modelpb.FieldOpts, field *protogen.Field) (string, string) {
	if field.Desc.IsMap() {
		return "encoding/json", "Unmarshal"
	}
	if fieldOpts.GetAsJsonBytes() {
		return "github.com/malonaz/core/go/pbutil", "JSONUnmarshal"
	}
	return "github.com/malonaz/core/go/pbutil", "Unmarshal"
}

// --- FromPb field helpers ---

func (m *Model) fromPbFieldConversion(field *protogen.Field, fieldOpts *modelpb.FieldOpts, modelGoName string, pgvector string) (string, error) {
	var b strings.Builder
	goName := field.GoName
	nullable := fieldOpts.GetNullable()

	if fieldOpts.GetAsJsonBytes() || fieldOpts.GetAsProtoBytes() {
		lib, fn := m.marshalLib(fieldOpts, field)
		if nullable {
			fmt.Fprintf(&b, "\tvar %sBytes []byte\n", goName)
			fmt.Fprintf(&b, "\tif m.%s != nil {\n", goName)
			fmt.Fprintf(&b, "\t\tvar err error\n")
			fmt.Fprintf(&b, "\t\t%sBytes, err = %s(m.%s)\n", goName, m.fqn(lib, fn), goName)
			fmt.Fprintf(&b, "\t\tif err != nil {\n")
			fmt.Fprintf(&b, "\t\t\treturn nil, %s(\"marshaling %s: %%w\", err)\n", m.fqn("fmt", "Errorf"), goName)
			fmt.Fprintf(&b, "\t\t}\n")
			fmt.Fprintf(&b, "\t}\n")
		} else {
			fmt.Fprintf(&b, "\tif m.%s == nil {\n", goName)
			fmt.Fprintf(&b, "\t\treturn nil, %s(\"%s cannot be nil\")\n", m.fqn("fmt", "Errorf"), goName)
			fmt.Fprintf(&b, "\t}\n")
			fmt.Fprintf(&b, "\t%sBytes, err := %s(m.%s)\n", goName, m.fqn(lib, fn), goName)
			fmt.Fprintf(&b, "\tif err != nil {\n")
			fmt.Fprintf(&b, "\t\treturn nil, %s(\"marshaling %s: %%w\", err)\n", m.fqn("fmt", "Errorf"), goName)
			fmt.Fprintf(&b, "\t}\n")
		}
		return b.String(), nil
	}

	if fieldOpts.GetPgVector() {
		if nullable {
			fmt.Fprintf(&b, "\tvar %sPtr *%s\n", goName, m.fqn(pgvector, "Vector"))
			fmt.Fprintf(&b, "\tif m.%s != nil {\n", goName)
			fmt.Fprintf(&b, "\t\t%s := %s(m.%s)\n", goName, m.fqn(pgvector, "NewVector"), goName)
			fmt.Fprintf(&b, "\t\t%sPtr = &%s\n", goName, goName)
			fmt.Fprintf(&b, "\t}\n")
		}
		return b.String(), nil
	}

	if fieldOpts.GetEmbed() {
		fmt.Fprintf(&b, "\t%s, err := %s%sFromPb(m.%s)\n", goName, modelGoName, goName, goName)
		fmt.Fprintf(&b, "\tif err != nil {\n")
		fmt.Fprintf(&b, "\t\treturn nil, %s(\"converting to model %s: %%w\", err)\n", m.fqn("fmt", "Errorf"), field.Desc.TextName())
		fmt.Fprintf(&b, "\t}\n")
		return b.String(), nil
	}

	if nullable {
		sanitizedType, err := protofield.SanitizedGoType(field, m.fqn)
		if err != nil {
			return "", fmt.Errorf("resolving sanitized go type for %s: %w", goName, err)
		}
		fmt.Fprintf(&b, "\tvar %s *%s\n", goName, sanitizedType)

		if protofield.IsTimestamp(field) || protofield.IsDuration(field) {
			fmt.Fprintf(&b, "\tif m.%s != nil {\n", goName)
		} else {
			zv, err := protofield.ZeroValue(field)
			if err != nil {
				return "", fmt.Errorf("resolving zero value for %s: %w", goName, err)
			}
			fmt.Fprintf(&b, "\tif m.%s != %s {\n", goName, zv)
		}

		if field.Enum != nil {
			fmt.Fprintf(&b, "\t\t%sInt := int16(m.%s)\n", goName, goName)
			fmt.Fprintf(&b, "\t\t%s = &%sInt\n", goName, goName)
		} else if protofield.IsTimestamp(field) {
			fmt.Fprintf(&b, "\t\tif err := m.%s.CheckValid(); err != nil {\n", goName)
			fmt.Fprintf(&b, "\t\t\treturn nil, %s(\"validating %s: %%w\", err)\n", m.fqn("fmt", "Errorf"), field.Desc.TextName())
			fmt.Fprintf(&b, "\t\t}\n")
			fmt.Fprintf(&b, "\t\tt := m.%s.AsTime()\n", goName)
			fmt.Fprintf(&b, "\t\t%s = &t\n", goName)
		} else if protofield.IsDuration(field) {
			fmt.Fprintf(&b, "\t\tif err := m.%s.CheckValid(); err != nil {\n", goName)
			fmt.Fprintf(&b, "\t\t\treturn nil, %s(\"validating %s: %%w\", err)\n", m.fqn("fmt", "Errorf"), field.Desc.TextName())
			fmt.Fprintf(&b, "\t\t}\n")
			fmt.Fprintf(&b, "\t\td := m.%s.AsDuration()\n", goName)
			fmt.Fprintf(&b, "\t\t%s = &d\n", goName)
		} else {
			fmt.Fprintf(&b, "\t\t%s = &m.%s\n", goName, goName)
		}

		fmt.Fprintf(&b, "\t}\n")
		return b.String(), nil
	}

	if protofield.IsTimestamp(field) || protofield.IsDuration(field) {
		fmt.Fprintf(&b, "\tif err := m.%s.CheckValid(); err != nil {\n", goName)
		fmt.Fprintf(&b, "\t\treturn nil, %s(\"validating %s: %%w\", err)\n", m.fqn("fmt", "Errorf"), field.Desc.TextName())
		fmt.Fprintf(&b, "\t}\n")
	}

	return b.String(), nil
}

func (m *Model) fromPbFieldValue(field *protogen.Field, fieldOpts *modelpb.FieldOpts, fieldPrefix string) string {
	goName := field.GoName
	nullable := fieldOpts.GetNullable()

	if fieldOpts.GetAsJsonBytes() || fieldOpts.GetAsProtoBytes() {
		return goName + "Bytes"
	}
	if fieldOpts.GetPgVector() {
		if nullable {
			return goName + "Ptr"
		}
		return m.fqn("github.com/pgvector/pgvector-go", "NewVector") + "(m." + goName + ")"
	}
	if fieldOpts.GetEmbed() {
		return goName
	}
	if nullable {
		return goName
	}
	return m.protoFieldValue(field)
}

func (m *Model) protoFieldValue(field *protogen.Field) string {
	if field.Enum != nil {
		return fmt.Sprintf("int16(m.%s)", field.GoName)
	}
	if protofield.IsTimestamp(field) {
		return fmt.Sprintf("m.%s.AsTime()", field.GoName)
	}
	if protofield.IsDuration(field) {
		return fmt.Sprintf("m.%s.AsDuration()", field.GoName)
	}
	return "m." + field.GoName
}

// --- ToPb field helpers ---

func (m *Model) toPbFieldConversion(field *protogen.Field, fieldOpts *modelpb.FieldOpts, modelGoName string) (string, error) {
	var b strings.Builder
	goName := field.GoName
	nullable := fieldOpts.GetNullable()

	if fieldOpts.GetAsJsonBytes() || fieldOpts.GetAsProtoBytes() {
		lib, fn := m.unmarshalLib(fieldOpts, field)
		if nullable {
			if field.Desc.IsMap() {
				mapType := fmt.Sprintf("map[%s]%s", field.Desc.MapKey().Kind(), field.Desc.MapValue().Kind())
				fmt.Fprintf(&b, "\tvar %s %s\n", goName, mapType)
				fmt.Fprintf(&b, "\tif m.%s != nil {\n", goName)
				fmt.Fprintf(&b, "\t\t%s = %s{}\n", goName, mapType)
				fmt.Fprintf(&b, "\t\tif err := %s(m.%s, &%s); err != nil {\n", m.fqn(lib, fn), goName, goName)
				fmt.Fprintf(&b, "\t\t\treturn nil, %s(\"unmarshaling %s: %%w\", err)\n", m.fqn("fmt", "Errorf"), goName)
				fmt.Fprintf(&b, "\t\t}\n")
				fmt.Fprintf(&b, "\t}\n")
			} else {
				protoType := m.qgi(field.Message.GoIdent)
				fmt.Fprintf(&b, "\tvar %s *%s\n", goName, protoType)
				fmt.Fprintf(&b, "\tif m.%s != nil {\n", goName)
				fmt.Fprintf(&b, "\t\t%s = &%s{}\n", goName, protoType)
				fmt.Fprintf(&b, "\t\tif err := %s(m.%s, %s); err != nil {\n", m.fqn(lib, fn), goName, goName)
				fmt.Fprintf(&b, "\t\t\treturn nil, %s(\"unmarshaling %s: %%w\", err)\n", m.fqn("fmt", "Errorf"), goName)
				fmt.Fprintf(&b, "\t\t}\n")
				fmt.Fprintf(&b, "\t}\n")
			}
		} else {
			unmarshalPrefix := ""
			if field.Desc.IsMap() {
				unmarshalPrefix = "&"
				fmt.Fprintf(&b, "\t%s := map[%s]%s{}\n", goName, field.Desc.MapKey().Kind(), field.Desc.MapValue().Kind())
			} else {
				fmt.Fprintf(&b, "\t%s := &%s{}\n", goName, m.qgi(field.Message.GoIdent))
			}
			fmt.Fprintf(&b, "\tif err := %s(m.%s, %s%s); err != nil {\n", m.fqn(lib, fn), goName, unmarshalPrefix, goName)
			fmt.Fprintf(&b, "\t\treturn nil, %s(\"unmarshaling %s: %%w\", err)\n", m.fqn("fmt", "Errorf"), goName)
			fmt.Fprintf(&b, "\t}\n")
		}
		return b.String(), nil
	}

	if fieldOpts.GetPgVector() && nullable {
		sanitizedType, err := protofield.SanitizedGoType(field, m.fqn)
		if err != nil {
			return "", fmt.Errorf("resolving sanitized go type for %s: %w", goName, err)
		}
		fmt.Fprintf(&b, "\tvar %s %s\n", goName, sanitizedType)
		fmt.Fprintf(&b, "\tif m.%s != nil {\n", goName)
		fmt.Fprintf(&b, "\t\t%s = m.%s.Slice()\n", goName, goName)
		fmt.Fprintf(&b, "\t}\n")
		return b.String(), nil
	}

	if fieldOpts.GetEmbed() {
		fmt.Fprintf(&b, "\t%s, err := m.%s%s.ToPb()\n", goName, modelGoName, goName)
		fmt.Fprintf(&b, "\tif err != nil {\n")
		fmt.Fprintf(&b, "\t\treturn nil, %s(\"converting to pb %s: %%w\", err)\n", m.fqn("fmt", "Errorf"), goName)
		fmt.Fprintf(&b, "\t}\n")
		return b.String(), nil
	}

	if protofield.IsTimestamp(field) {
		tsPkg := "google.golang.org/protobuf/types/known/timestamppb"
		fmt.Fprintf(&b, "\tvar %s *%s\n", goName, m.fqn(tsPkg, "Timestamp"))
		if nullable {
			fmt.Fprintf(&b, "\tif m.%s != nil {\n", goName)
		}
		deref := ""
		if nullable {
			deref = "*"
		}
		fmt.Fprintf(&b, "\t%s = %s(%sm.%s)\n", goName, m.fqn(tsPkg, "New"), deref, goName)
		fmt.Fprintf(&b, "\tif err := %s.CheckValid(); err != nil {\n", goName)
		fmt.Fprintf(&b, "\t\treturn nil, %s(\"validating %s: %%w\", err)\n", m.fqn("fmt", "Errorf"), field.Desc.TextName())
		fmt.Fprintf(&b, "\t}\n")
		if nullable {
			fmt.Fprintf(&b, "\t}\n")
		}
		return b.String(), nil
	}

	if protofield.IsDuration(field) {
		durPkg := "google.golang.org/protobuf/types/known/durationpb"
		fmt.Fprintf(&b, "\tvar %s *%s\n", goName, m.fqn(durPkg, "Duration"))
		if nullable {
			fmt.Fprintf(&b, "\tif m.%s != nil {\n", goName)
		}
		deref := ""
		if nullable {
			deref = "*"
		}
		fmt.Fprintf(&b, "\t%s = %s(%sm.%s)\n", goName, m.fqn(durPkg, "New"), deref, goName)
		fmt.Fprintf(&b, "\tif err := %s.CheckValid(); err != nil {\n", goName)
		fmt.Fprintf(&b, "\t\treturn nil, %s(\"validating %s: %%w\", err)\n", m.fqn("fmt", "Errorf"), field.Desc.TextName())
		fmt.Fprintf(&b, "\t}\n")
		if nullable {
			fmt.Fprintf(&b, "\t}\n")
		}
		return b.String(), nil
	}

	if nullable {
		sanitizedType, err := protofield.SanitizedGoType(field, m.fqn)
		if err != nil {
			return "", fmt.Errorf("resolving sanitized go type for %s: %w", goName, err)
		}
		fmt.Fprintf(&b, "\tvar %s %s\n", goName, sanitizedType)
		fmt.Fprintf(&b, "\tif m.%s != nil {\n", goName)
		fmt.Fprintf(&b, "\t\t%s = *m.%s\n", goName, goName)
		fmt.Fprintf(&b, "\t}\n")
		return b.String(), nil
	}

	return "", nil
}

func (m *Model) toPbFieldValue(field *protogen.Field, fieldOpts *modelpb.FieldOpts) string {
	goName := field.GoName
	nullable := fieldOpts.GetNullable()

	if fieldOpts.GetAsJsonBytes() || fieldOpts.GetAsProtoBytes() {
		return goName
	}
	if fieldOpts.GetPgVector() {
		if nullable {
			return goName
		}
		return "m." + goName + ".Slice()"
	}
	if fieldOpts.GetEmbed() {
		return goName
	}
	if protofield.IsTimestamp(field) || protofield.IsDuration(field) {
		return goName
	}
	if nullable {
		if field.Enum != nil {
			return fmt.Sprintf("%s(%s)", m.qgi(field.Enum.GoIdent), goName)
		}
		return goName
	}
	return m.modelFieldValue(field)
}

func (m *Model) modelFieldValue(field *protogen.Field) string {
	if field.Enum != nil {
		return fmt.Sprintf("%s(m.%s)", m.qgi(field.Enum.GoIdent), field.GoName)
	}
	return "m." + field.GoName
}
