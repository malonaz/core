package postgres

import (
	"errors"
	"fmt"
	"strings"

	"github.com/huandu/xstrings"
	"google.golang.org/protobuf/compiler/protogen"

	modelpb "github.com/malonaz/core/genproto/codegen/model/v1"
	"github.com/malonaz/core/go/pbutil"
	"github.com/malonaz/core/tools/protoc-gen-core/plugin"
	"github.com/malonaz/core/tools/protoc-gen-core/resource"
)

var (
	postgresPkg = protogen.GoImportPath("github.com/malonaz/core/go/postgres")
	pgxPkg      = protogen.GoImportPath("github.com/jackc/pgx/v5")
	fmtPkg      = protogen.GoImportPath("fmt")
	stringsPkg  = protogen.GoImportPath("strings")
	timePkg     = protogen.GoImportPath("time")
	contextPkg  = protogen.GoImportPath("context")

	storeGenerated bool
)

func ResetState() {
	storeGenerated = false
}

func Generate(file *protogen.File, g *protogen.GeneratedFile, packageName protogen.GoPackageName, opts *plugin.Opts) error {
	modelImportPath := opts.AdditionalGoImportPaths["model"]
	type modelMessage struct {
		message   *protogen.Message
		modelOpts *modelpb.ModelOpts
		pr        *resource.ParsedResource
	}

	var messages []modelMessage
	for _, message := range file.Messages {
		modelOpts, err := pbutil.GetExtension[*modelpb.ModelOpts](message.Desc.Options(), modelpb.E_ModelOpts)
		if err != nil {
			if errors.Is(err, pbutil.ErrExtensionNotFound) {
				continue
			}
			return fmt.Errorf("getting model_opts for %s: %w", message.GoIdent.GoName, err)
		}
		pr, err := resource.ParseFromMessage(message)
		if err != nil {
			return fmt.Errorf("parsing resource from message %s: %w", message.GoIdent.GoName, err)
		}
		messages = append(messages, modelMessage{message: message, modelOpts: modelOpts, pr: pr})
	}

	if len(messages) == 0 {
		g.Skip()
		return nil
	}

	gen := &generator{g: g, modelImportPath: modelImportPath}
	_ = gen.ident(contextPkg, "context")

	g.P("// Code generated by protoc-gen-api. DO NOT EDIT.")
	g.P("// source: ", file.Desc.Path())
	g.P("package ", packageName)
	g.P()

	if !storeGenerated {
		storeGenerated = true
		g.P("// Store is this service's db layer.")
		g.P("type Store struct {")
		g.P("  client *", postgresPkg.Ident("Client"))
		g.P("}")
		g.P()
		g.P("// New returns a new db.")
		g.P("func New(client *", postgresPkg.Ident("Client"), ") *Store {")
		g.P("  return &Store{client: client}")
		g.P("}")
		g.P()
	}

	g.P("var (")
	for _, mm := range messages {
		goType := mm.message.GoIdent.GoName
		g.P("  ", goType, "PostgresColumns = ", postgresPkg.Ident("GetDBColumns"), "(", modelImportPath.Ident(goType), "{})")
	}
	g.P(")")
	g.P()

	for _, mm := range messages {
		mc, err := gen.newMsgCtx(mm.message, mm.modelOpts, mm.pr)
		if err != nil {
			return fmt.Errorf("building context for %s: %w", mm.message.GoIdent.GoName, err)
		}
		mc.generateETagGetter()
		mc.generateInsertVars()
		mc.generateInsert()
		mc.generateWithRequestIDStruct()
		mc.generateInsertIdempotently()
		mc.generateUpdate()
		mc.generateDelete()
		mc.generateGet()
		mc.generateList()
	}

	return nil
}

type generator struct {
	g               *protogen.GeneratedFile
	modelImportPath protogen.GoImportPath
}

func (gen *generator) ident(path protogen.GoImportPath, name string) string {
	return gen.g.QualifiedGoIdent(protogen.GoIdent{GoName: name, GoImportPath: path})
}

func (gen *generator) modelIdent(name string) string {
	return gen.ident(gen.modelImportPath, name)
}

type singletonChild struct {
	pr        *resource.ParsedResource
	message   *protogen.Message
	modelOpts *modelpb.ModelOpts
}

// msgCtx holds all precomputed values for generating postgres code for a single message.
type msgCtx struct {
	gen *generator
	g   *protogen.GeneratedFile

	message   *protogen.Message
	modelOpts *modelpb.ModelOpts
	pr        *resource.ParsedResource

	goType  string
	goName  string
	goParam string

	goTypeFqi         string
	errNotExist       string
	errAlreadyExists  string
	errAlreadyDeleted string
	errETagChanged    string

	tableName        string
	whereClause      string
	placeholderDecls string
	columnNames      string
	identifier       string

	hasEtag       bool
	hasDeleteTime bool

	singletonChildren []singletonChild
}

func (gen *generator) newMsgCtx(message *protogen.Message, modelOpts *modelpb.ModelOpts, pr *resource.ParsedResource) (*msgCtx, error) {
	goType := message.GoIdent.GoName
	goName := untitle(goType)

	tableName := pr.SingularSnakeCase()
	if modelOpts.GetTableName() != "" {
		tableName = modelOpts.GetTableName()
	}
	if modelOpts.GetSchemaName() != "" {
		tableName = modelOpts.GetSchemaName() + "." + tableName
	}

	patternVars := pr.PatternVariables
	lastIdx := len(patternVars) - 1

	var whereConditions, placeholderConditions []string
	for i, v := range patternVars {
		col := v + "_id"
		if i == lastIdx && modelOpts.GetIdColumnName() != "" {
			col = modelOpts.GetIdColumnName()
		}
		whereConditions = append(whereConditions, fmt.Sprintf("%s = $%%d", col))
		placeholderConditions = append(placeholderConditions, fmt.Sprintf("%s = $%d", col, i+1))
	}

	columnNames := pr.PatternVariableIDs(false)
	identifier := ""
	if !pr.Singleton {
		id, err := pr.PatternVariableID(false)
		if err != nil {
			return nil, err
		}
		identifier = id
	}
	if modelOpts.GetIdColumnName() != "" {
		id, err := pr.PatternVariableID(false)
		if err != nil {
			return nil, err
		}
		identifier = modelOpts.GetIdColumnName()
		columnNames = strings.Replace(columnNames, id, modelOpts.GetIdColumnName(), 1)
	}

	var singletonChildren []singletonChild
	for _, childPr := range pr.Children {
		if !childPr.Singleton {
			continue
		}
		childMessage, err := resource.GetMessageByResourceType(childPr.Desc.Type)
		if err != nil {
			continue
		}
		childModelOpts, err := pbutil.GetExtension[*modelpb.ModelOpts](childMessage.Desc.Options(), modelpb.E_ModelOpts)
		if err != nil {
			continue
		}
		singletonChildren = append(singletonChildren, singletonChild{pr: childPr, message: childMessage, modelOpts: childModelOpts})
	}

	return &msgCtx{
		gen:     gen,
		g:       gen.g,
		message: message, modelOpts: modelOpts, pr: pr,

		goType:  goType,
		goName:  goName,
		goParam: "_" + goName,

		goTypeFqi:         gen.modelIdent(goType),
		errNotExist:       gen.modelIdent("Err" + goType + "NotExist"),
		errAlreadyExists:  gen.modelIdent("Err" + goType + "AlreadyExists"),
		errAlreadyDeleted: gen.modelIdent("Err" + goType + "AlreadyDeleted"),
		errETagChanged:    gen.modelIdent("Err" + goType + "ETagChanged"),

		tableName:        tableName,
		whereClause:      strings.Join(whereConditions, " AND "),
		placeholderDecls: strings.Join(placeholderConditions, " AND "),
		columnNames:      columnNames,
		identifier:       identifier,

		hasEtag:       message.Desc.Fields().ByName("etag") != nil,
		hasDeleteTime: message.Desc.Fields().ByName("delete_time") != nil,

		singletonChildren: singletonChildren,
	}, nil
}

// Shorthand ident helpers on msgCtx for commonly used packages.
func (mc *msgCtx) pgx(name string) string      { return mc.gen.ident(pgxPkg, name) }
func (mc *msgCtx) postgres(name string) string { return mc.gen.ident(postgresPkg, name) }
func (mc *msgCtx) fmtI(name string) string     { return mc.gen.ident(fmtPkg, name) }
func (mc *msgCtx) stringsI(name string) string { return mc.gen.ident(stringsPkg, name) }

func (mc *msgCtx) patternVarIDsGoTrue() string { return mc.pr.PatternVariableIDs(true) }

func (mc *msgCtx) patternVarFieldAccess() string {
	var parts []string
	for _, v := range mc.pr.PatternVariables {
		camel := xstrings.ToCamelCase(v)
		parts = append(parts, fmt.Sprintf("%s.%sID", mc.goParam, title(camel)))
	}
	return strings.Join(parts, ", ")
}

func (mc *msgCtx) patternVarIDUntitled() string {
	var parts []string
	for _, v := range mc.pr.PatternVariables {
		camel := untitle(xstrings.ToCamelCase(v))
		parts = append(parts, camel+"Id")
	}
	return strings.Join(parts, ", ")
}

// generateETagGetter emits the private etag lookup helper when the message has an etag field.
func (mc *msgCtx) generateETagGetter() {
	if !mc.hasEtag {
		return
	}
	g := mc.g
	g.P(fmt.Sprintf("func (s *Store) get%sETag(ctx context.Context, %s string) (string, error) {", mc.goType, mc.patternVarIDsGoTrue()))
	g.P(fmt.Sprintf("  query := `SELECT etag FROM %s WHERE %s`", mc.tableName, mc.placeholderDecls))
	g.P(fmt.Sprintf("  rows, err := s.client.Query(ctx, query, %s)", mc.patternVarIDsGoTrue()))
	g.P("  if err != nil {")
	g.P("    return \"\", err")
	g.P("  }")
	g.P(fmt.Sprintf("  return %s(rows, %s[string])", mc.pgx("CollectOneRow"), mc.pgx("RowTo")))
	g.P("}")
	g.P()
}

// generateETagCheck emits the etag mismatch detection block used inside ErrNoRows handlers.
func (mc *msgCtx) generateETagCheck(operation string, patternVarArgs string) {
	g := mc.g
	g.P("      if etag != \"\" {")
	g.P(fmt.Sprintf("        currentEtag, getEtagErr := s.get%sETag(ctx, %s)", mc.goType, patternVarArgs))
	g.P("        switch getEtagErr {")
	g.P("        case nil:")
	g.P("          if currentEtag == etag {")
	g.P("            return nil, ", mc.fmtI("Errorf"), "(\"", operation, " matched no rows but etag unchanged: expected etag mismatch\")")
	g.P("          }")
	g.P(fmt.Sprintf("          return nil, %s", mc.errETagChanged))
	g.P(fmt.Sprintf("        case %s:", mc.pgx("ErrNoRows")))
	g.P(fmt.Sprintf("          return nil, %s", mc.errNotExist))
	g.P("        default:")
	g.P("          return nil, ", mc.fmtI("Errorf"), "(\"getting etag: %v\", getEtagErr)")
	g.P("        }")
	g.P("      }")
}

func untitle(s string) string {
	if s == "" {
		return s
	}
	return strings.ToLower(s[:1]) + s[1:]
}

func title(s string) string {
	if s == "" {
		return s
	}
	return strings.ToUpper(s[:1]) + s[1:]
}
