// serviceInfo holds the collected info for a single service.
type serviceInfo struct {
	service       *protogen.Service
	resources     []*resource.ParsedResource
	natsStream    bool
	natsResources map[string]bool // resource types that require nats client
}

// methodInfo holds the collected info for a single RPC method.
type methodInfo struct {
	method        *protogen.Method
	rpc           *resource.RPC
	natsEventOpts *codegennatspb.EventOptions
}

func Generate(file *protogen.File, g *protogen.GeneratedFile, packageName protogen.GoPackageName, opts *plugin.Opts) error {
	modelImportPath := opts.AdditionalGoImportPaths["model"]

	gen := &generator{g: g, modelImportPath: modelImportPath, file: file}
	// Force context import.
	_ = gen.ident(contextPkg, "Context")

	// Phase 1: Scan services and methods.
	var services []*serviceInfo
	type methodEntry struct {
		si *serviceInfo
		mi *methodInfo
	}
	var allMethods []methodEntry

	for _, svc := range file.Services {
		si := &serviceInfo{
			service:       svc,
			natsResources: map[string]bool{},
		}
		streamOptionsList, err := pbutil.GetExtension[[]*natspb.StreamOptions](svc.Desc.Options(), codegennatspb.E_Stream)
		if err != nil && !errors.Is(err, pbutil.ErrExtensionNotFound) {
			return fmt.Errorf("getting stream extension for %s: %w", svc.GoName, err)
		}
		si.natsStream = len(streamOptionsList) > 0

		resourceSet := map[string]bool{}
		for _, method := range svc.Methods {
			rpc, err := resource.ParseRPC(method)
			if err != nil {
				return fmt.Errorf("parsing rpc %s: %w", method.GoName, err)
			}
			if rpc == nil {
				continue
			}
			pr := rpc.ParsedResource

			natsEventOpts, err := getNatsEventOpts(rpc.Message)
			if err != nil {
				return err
			}
			if natsEventOpts != nil {
				if !si.natsStream {
					return fmt.Errorf("resource %s has nats event annotation but service %s defines no such stream", pr.Desc.Type, svc.GoName)
				}
				si.natsResources[pr.Desc.Type] = true
			}

			if !resourceSet[pr.Desc.Singular] {
				resourceSet[pr.Desc.Singular] = true
				si.resources = append(si.resources, pr)
			}

			mi := &methodInfo{method: method, rpc: rpc, natsEventOpts: natsEventOpts}
			allMethods = append(allMethods, methodEntry{si: si, mi: mi})
		}
		if len(si.resources) > 0 {
			services = append(services, si)
		}
	}

	if len(services) == 0 {
		g.Skip()
		return nil
	}

	g.P("// Code generated by protoc-gen-api. DO NOT EDIT.")
	g.P("// source: ", file.Desc.Path())
	g.P("package ", packageName)
	g.P()

	// Phase 2: Generate service-level code.
	for _, si := range services {
		gen.generateServiceLevel(si)
	}

	// Phase 3: Generate per-resource interfaces, server structs, and per-method implementations.
	generatedResources := map[string]bool{}
	for _, entry := range allMethods {
		si := entry.si
		mi := entry.mi
		pr := mi.rpc.ParsedResource

		if !generatedResources[pr.Desc.Singular] {
			generatedResources[pr.Desc.Singular] = true
			if err := gen.generateResourceLevel(si, mi, file); err != nil {
				return err
			}
		}

		if err := gen.generateMethod(si, mi); err != nil {
			return fmt.Errorf("generating %s: %w", mi.method.GoName, err)
		}
	}

	return nil
}

type generator struct {
	g               *protogen.GeneratedFile
	modelImportPath protogen.GoImportPath
	file            *protogen.File
}

func (gen *generator) ident(path protogen.GoImportPath, name string) string {
	return gen.g.QualifiedGoIdent(protogen.GoIdent{GoName: name, GoImportPath: path})
}

func (gen *generator) modelIdent(name string) string {
	return gen.ident(gen.modelImportPath, name)
}

func (gen *generator) fileIdent(name string) string {
	return gen.g.QualifiedGoIdent(gen.file.GoImportPath.Ident(name))
}

func (gen *generator) qgi(ident protogen.GoIdent) string {
	return gen.g.QualifiedGoIdent(ident)
}

func (gen *generator) resourcePkgIdent(msg *protogen.Message, name string) string {
	return gen.g.QualifiedGoIdent(msg.GoIdent.GoImportPath.Ident(name))
}

// generateServiceLevel emits the combined store interface, server struct, constructor, and Start method.
func (gen *generator) generateServiceLevel(si *serviceInfo) {
	g := gen.g
	svcName := si.service.GoName
	svcNameUntitled := xstrings.ToCamelCase(svcName)

	// Combined store interface.
	g.P(fmt.Sprintf("type %sStore interface {", svcNameUntitled))
	for _, pr := range si.resources {
		g.P(fmt.Sprintf("  %s_%sStore", svcNameUntitled, pr.SingularGoName()))
	}
	g.P("}")
	g.P()

	// Server struct.
	g.P(fmt.Sprintf("type %sServer struct {", svcName))
	if si.natsStream {
		g.P(fmt.Sprintf("  natsClient *%s", gen.ident(natsPkg, "Client")))
	}
	for _, pr := range si.resources {
		g.P(fmt.Sprintf("  *%s_%sServer", svcNameUntitled, pr.SingularGoName()))
	}
	g.P("}")
	g.P()

	// Constructor.
	natsParam := ""
	if si.natsStream {
		natsParam = fmt.Sprintf(", natsClient *%s", gen.ident(natsPkg, "Client"))
	}
	g.P(fmt.Sprintf("func New%sServer(store %sStore%s) *%sServer {", svcName, svcNameUntitled, natsParam, svcName))
	g.P(fmt.Sprintf("  return &%sServer{", svcName))
	if si.natsStream {
		g.P("    natsClient: natsClient,")
	}
	for _, pr := range si.resources {
		natsArg := ""
		if si.natsResources[pr.Desc.Type] {
			natsArg = ", natsClient"
		}
		g.P(fmt.Sprintf("    %s_%sServer: new%s_%sServer(store%s),",
			svcNameUntitled, pr.SingularGoName(),
			svcName, pr.SingularGoName(), natsArg))
	}
	g.P("  }")
	g.P("}")
	g.P()

	// Start method.
	g.P(fmt.Sprintf("func (s *%sServer) Start(ctx %s) error {", svcName, gen.ident(contextPkg, "Context")))
	if si.natsStream {
		g.P(fmt.Sprintf("  streamOptionsList, err := %s[[]*%s](%s.ServiceName, %s)",
			gen.ident(pbutilPkg, "GetServiceOption"),
			gen.ident(natsGenPkg, "StreamOptions"),
			gen.qgi(gen.file.GoImportPath.Ident(svcName+"_ServiceDesc")),
			gen.ident(natsCodegenPkg, "E_Stream")))
		g.P("  if err != nil {")
		g.P(fmt.Sprintf("    return %s(\"getting stream options: %%w\", err)", gen.ident(fmtPkg, "Errorf")))
		g.P("  }")
		g.P("  for _, streamOptions := range streamOptionsList {")
		g.P(fmt.Sprintf("    if _, err := s.natsClient.CreateOrUpdateStream(ctx, %s(streamOptions)); err != nil {",
			gen.ident(natsPkg, "NewStream")))
		g.P(fmt.Sprintf("      return %s(\"creating stream %%q: %%w\", streamOptions.GetName(), err)",
			gen.ident(fmtPkg, "Errorf")))
		g.P("    }")
		g.P("  }")
	}
	g.P("  return nil")
	g.P("}")
	g.P()
}

// generateResourceLevel emits the per-resource store interface, server struct, and constructor.
func (gen *generator) generateResourceLevel(si *serviceInfo, mi *methodInfo, file *protogen.File) error {
	g := gen.g
	pr := mi.rpc.ParsedResource
	svcName := xstrings.ToPascalCase(si.service.GoName)
	svcNameUntitled := xstrings.ToCamelCase(si.service.GoName)
	resourceGoName := pr.SingularGoName()
	storeIface := fmt.Sprintf("%s_%sStore", svcNameUntitled, resourceGoName)
	serverNameUntitled := fmt.Sprintf("%s_%sServer", svcNameUntitled, resourceGoName)
	serverName := fmt.Sprintf("%s_%sServer", svcName, resourceGoName)
	goTypeQgi := gen.modelIdent(mi.rpc.Message.GoIdent.GoName)

	softDeletable := mi.rpc.Message.Desc.Fields().ByName("delete_time") != nil
	hasEtag := mi.rpc.Message.Desc.Fields().ByName("etag") != nil

	singletonChildren, err := getSingletonChildren(pr)
	if err != nil {
		return err
	}

	// Determine hasRequestID by scanning all create methods for this resource.
	hasRequestID := false
	for _, method := range si.service.Methods {
		rpc, err := resource.ParseRPC(method)
		if err != nil || rpc == nil {
			continue
		}
		if rpc.Create && rpc.ParsedResource.Desc.Singular == pr.Desc.Singular {
			hasRequestID = method.Input.Desc.Fields().ByName("request_id") != nil
			break
		}
	}

	natsEventOpts, err := getNatsEventOpts(mi.rpc.Message)
	if err != nil {
		return err
	}

	// Store interface.
	g.P(fmt.Sprintf("type %s interface {", storeIface))

	// Insert
	insertSig := fmt.Sprintf("  Insert%s", resourceGoName)
	if hasRequestID {
		insertSig += "Idempotently"
	}
	insertSig += fmt.Sprintf("(ctx %s, ", gen.ident(contextPkg, "Context"))
	if hasRequestID {
		insertSig += "requestID string, "
	}
	insertSig += fmt.Sprintf("%s *%s", xstrings.ToCamelCase(resourceGoName), goTypeQgi)
	for _, childPr := range singletonChildren {
		insertSig += fmt.Sprintf(", %s *%s", xstrings.ToCamelCase(childPr.SingularGoName()), gen.modelIdent(childPr.SingularGoName()))
	}
	insertSig += fmt.Sprintf(") (*%s, error)", goTypeQgi)
	g.P(insertSig)

	// Update
	updateSig := fmt.Sprintf("  Update%s(ctx %s, %s *%s, updateClause string, columns []string",
		resourceGoName, gen.ident(contextPkg, "Context"), xstrings.ToCamelCase(resourceGoName), goTypeQgi)
	if hasEtag {
		updateSig += ", etag string"
	}
	updateSig += fmt.Sprintf(") (*%s, error)", goTypeQgi)
	g.P(updateSig)

	// Delete (only for non-singletons).
	if !pr.Singleton {
		if softDeletable {
			deleteSig := fmt.Sprintf("  SoftDelete%s(ctx %s, %s string",
				resourceGoName, gen.ident(contextPkg, "Context"), pr.PatternVariableIDs(true))
			if hasEtag {
				deleteSig += ", etag, newEtag string"
			}
			deleteSig += fmt.Sprintf(", deleteTime %s) (*%s, error)", gen.ident(timePkg, "Time"), goTypeQgi)
			g.P(deleteSig)
		} else {
			deleteSig := fmt.Sprintf("  Delete%s(ctx %s, %s string",
				resourceGoName, gen.ident(contextPkg, "Context"), pr.PatternVariableIDs(true))
			if hasEtag {
				deleteSig += ", etag string"
			}
			deleteSig += fmt.Sprintf(") (*%s, error)", goTypeQgi)
			g.P(deleteSig)
		}
	}

	// Get
	g.P(fmt.Sprintf("  Get%s(ctx %s, %s string) (*%s, error)",
		resourceGoName, gen.ident(contextPkg, "Context"), pr.PatternVariableIDs(true), goTypeQgi))

	// List
	listSig := fmt.Sprintf("  List%s(ctx %s, ", pr.PluralGoName(), gen.ident(contextPkg, "Context"))
	if pr.Parent != nil {
		listSig += pr.Parent.PatternVariableIDs(true) + " string, "
	}
	if softDeletable {
		listSig += "showDeleted bool, "
	}
	listSig += fmt.Sprintf("whereClause, orderByClause, paginationClause string, dbColumns []string, whereParams ...any) ([]*%s, error)", goTypeQgi)
	g.P(listSig)

	g.P("}")
	g.P()

	// Server struct.
	g.P(fmt.Sprintf("type %s struct {", serverNameUntitled))
	g.P(fmt.Sprintf("  store %s", storeIface))
	if natsEventOpts != nil {
		g.P(fmt.Sprintf("  natsClient *%s", gen.ident(natsPkg, "Client")))
	}
	g.P("}")
	g.P()

	// Constructor.
	constructorNats := ""
	if natsEventOpts != nil {
		constructorNats = fmt.Sprintf(", natsClient *%s", gen.ident(natsPkg, "Client"))
	}
	g.P(fmt.Sprintf("func new%s(store %s%s) *%s {",
		serverName, storeIface, constructorNats, serverNameUntitled))
	g.P(fmt.Sprintf("  return &%s{", serverNameUntitled))
	g.P("    store: store,")
	if natsEventOpts != nil {
		g.P("    natsClient: natsClient,")
	}
	g.P("  }")
	g.P("}")
	g.P()

	return nil
}

// generateMethod dispatches to the appropriate per-RPC generator.
func (gen *generator) generateMethod(si *serviceInfo, mi *methodInfo) error {
	mc := gen.newMethodCtx(si, mi)
	switch {
	case mi.rpc.Create:
		return mc.generateCreate()
	case mi.rpc.Update:
		return mc.generateUpdate()
	case mi.rpc.Delete:
		return mc.generateDelete()
	case mi.rpc.Get:
		mc.generateGet()
	case mi.rpc.BatchGet:
		return mc.generateBatchGet()
	case mi.rpc.List:
		mc.generateList()
	}
	return nil
}

// methodCtx holds all precomputed values for generating a single RPC method.
type methodCtx struct {
	gen *generator
	g   *protogen.GeneratedFile

	si *serviceInfo
	mi *methodInfo

	pr             *resource.ParsedResource
	resourceGoName string
	serverGoName   string
	modelGoName    string

	goTypeQgi         string
	parseFromPb       string
	parseName         string
	errNotExist       string
	errEtagChanged    string
	errAlreadyExists  string
	errAlreadyDeleted string

	softDeletable bool
	hasEtag       bool

	singletonChildren []*resource.ParsedResource
	natsStreamGoName  string
}

func (gen *generator) newMethodCtx(si *serviceInfo, mi *methodInfo) *methodCtx {
	pr := mi.rpc.ParsedResource
	resourceGoName := pr.SingularGoName()
	goType := mi.rpc.Message.GoIdent.GoName
	svcNameUntitled := xstrings.ToCamelCase(si.service.GoName)

	var natsStreamGoName string
	if mi.natsEventOpts != nil {
		natsStreamGoName = deriveNatsStreamGoName(mi.natsEventOpts.GetStream())
	}

	singletonChildren, _ := getSingletonChildren(pr)

	return &methodCtx{
		gen: gen,
		g:   gen.g,
		si:  si,
		mi:  mi,

		pr:             pr,
		resourceGoName: resourceGoName,
		serverGoName:   fmt.Sprintf("%s_%sServer", svcNameUntitled, resourceGoName),
		modelGoName:    resourceGoName + "Model",

		goTypeQgi:         gen.modelIdent(goType),
		parseFromPb:       gen.modelIdent(goType + "FromPb"),
		parseName:         gen.modelIdent("Parse" + goType + "Name"),
		errNotExist:       gen.modelIdent("Err" + goType + "NotExist"),
		errEtagChanged:    gen.modelIdent("Err" + goType + "ETagChanged"),
		errAlreadyExists:  gen.modelIdent("Err" + goType + "AlreadyExists"),
		errAlreadyDeleted: gen.modelIdent("Err" + goType + "AlreadyDeleted"),

		softDeletable: mi.rpc.Message.Desc.Fields().ByName("delete_time") != nil,
		hasEtag:       mi.rpc.Message.Desc.Fields().ByName("etag") != nil,

		singletonChildren: singletonChildren,
		natsStreamGoName:  natsStreamGoName,
	}
}

